// Contar cuántos números son mayores que 5
let contadorMayoresQue5 = 0;
for (let i = 1; i <= 10; i++) {
    if (i > 5) {
        contadorMayoresQue5++;
    }
}

// Imprimir el resultado
console.log(`Hay ${contadorMayoresQue5} números mayores que 5.`);
